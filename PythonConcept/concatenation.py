# Concatenation = combination of 2 same types of data

#valid concatenation statementas:

print("python" + "concepts")  # op- pythonconcepts
print(10 + 10)                  # op- 20
print(5.5 + 5.5)
print(5.5 + 10)
print(True + 20)             # In python True is considered as 1 and False as 0
print(False + 20)             # In python True is considered as 1 and False as 0
print(True + True)
print(False + False)
print(True + False)


#Invalid concatenation statementas:


print("python" + True)
print("python" + False)
print(True + 10.5)
print("python" + 3.12)     # Type Error